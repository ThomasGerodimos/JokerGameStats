    private void BtnGetRangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGetRangeActionPerformed
        lblResultList.setVisible(true);
        lstDrawsFromSearch.setVisible(true);
        lstDrawsFromSearch.setModel(mod);
        //Καθαρισμός λίστας απο προηγούμενη αναζήτηση (αν υπάρχουν)
        mod.clear();
        //Μορφοποίηση ημερομηνίας
        String pattern = "yyyy-MM-dd";
        DateFormat simpleDateFormat = new SimpleDateFormat(pattern);
        
        //Φόρτωση δεδομένων
        
        String urlToCall = "https://api.opap.gr/draws/v3.0/5104/draw-date/" + simpleDateFormat.format(txbDateFrom.getDate()) + "/" + simpleDateFormat.format(txbDateTo.getDate()) + "/draw-id";
        OkHttpClient client2 = new OkHttpClient();
        Request request2 = new Request.Builder().url(urlToCall).build();

        try (Response response2 = client2.newCall(request2).execute()) {
            if (response2.isSuccessful() && response2.body() != null) {
                //Parse Json from API

                String responseString2 = response2.body().string();

                String[] items = responseString2.replaceAll("\\[", "").replaceAll("\\]", "").replaceAll("\\s", "").split(",");
                int[] results = new int[items.length];
                //Αποθήκευση αριθμού κλήρωσης στην λίστα
                for (int i = 0; i < items.length; i++) {
                    try {
                        results[i] = Integer.parseInt(items[i]);
                        //Add Item
                        mod.addElement(results[i]);

                    } catch (NumberFormatException nfe) {
                        JOptionPane.showMessageDialog(this, "Παρουσιάστηκε κάποιο πρόβλημα. Παρακαλώ επικοινωνήστε με τοιν διαχειριστή!");
                    }
                }

            }
        } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Παρουσιάστηκε κάποιο πρόβλημα. Παρακαλώ επικοινωνήστε με τοιν διαχειριστή!");
        }
    }//GEN-LAST:event_BtnGetRangeActionPerformed

