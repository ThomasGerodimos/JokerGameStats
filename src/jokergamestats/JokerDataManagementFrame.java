    //Μέθοδος που αποθηκεύει στην Βάση Δεδομένων
    public void SaveDrawToDB(boolean RootMessageShow) {
        //Δημιουργία αντικειμένων EntityManagerFactory και EntityManager
        StartFrame.createEMFandEM();
        
        Query query1 = em.createQuery("SELECT count(s) FROM Games s");
        long count = (long) query1.getSingleResult();
        //Αν δεν υπάρχει καταχωρημένο το παιχνίδι Joker στον πίνακα των Games αποθηκευσε το αντικείμενο 
        //Joker που έχει δημιουρηθεί
        if (count == 0) {
            em.getTransaction().begin();
            try {
                em.persist(joker);
                em.getTransaction().commit();
            } catch (HeadlessException e) {
                //Αν παρουσιαστεί κάποιο πρόβλημα ακύρωσε τo transaction 
                em.getTransaction().rollback();
            } 

        }
        
        //Δημιούργησε ενα transaction για αποθηκευση τω νστοιχείων στην Βάση Δεδομένων
        em.getTransaction().begin();
        try {
            //Αποθήκευση της τρέχουσας κλήρωσης
            em.persist(CurrentDraw);
            //Αποθήκευση των αριθμών που κερδίζουν
            em.persist(CurrentWinningNumbers);
            //Αποθήκευση των κατηγοριών κερδών
            em.persist(Prizecategories0);
            em.persist(Prizecategories1);
            em.persist(Prizecategories2);
            em.persist(Prizecategories3);
            em.persist(Prizecategories4);
            em.persist(Prizecategories5);
            em.persist(Prizecategories6);
            em.persist(Prizecategories7);
            em.getTransaction().commit();
            //Αν η μέθοδος έχει κληθεί με παράμετρο true δείξε ετο μήνυμα επιτυχίας
            //Αυτό μπήκε γιατί στην περίπτωση που θα αποθηκεύει πολλές κληρώσεις θα εμφάνιζε πολλές φορές το μήνυμα και 
            //θα χρειάζοτανε η παρέμβαση του χρήστη για να συνεχιστεί η διαδικασία
            if (RootMessageShow == true) {
                JOptionPane.showMessageDialog(this, "Τα στοιχεία της κλήρωσης αποθηκεύτηκαν κανονικά!");
            }
        } catch (Exception e) {
              if (RootMessageShow == true) {
            JOptionPane.showMessageDialog(this, "Τα στοιχεία της κλήρωσης είναι ήδη αποθηκευμένα!");
              }
            em.getTransaction().rollback();
        } finally{
            //Με την ολοκλήρωση της διαδικασίας κλείνουμε τα αντικείμενα
            em.close();
            emf.close();        
    }

    }

    //Διαγραφή απο την Βάση Δεδομένων
    public void DeleteFromDB(boolean RootMessageShow) {
        //Δημιουργία αντικειμένων EntityManagerFactory και EntityManager
        StartFrame.createEMFandEM();

        em.getTransaction().begin();
        try {
            //Διαγραφή επιλεγμένης κλήρωσης
            Query query1 = em.createNamedQuery("Draws.DeleteByDrawid");
            query1.setParameter("drawid", parseInt(txbDrawID.getText()));
            query1.executeUpdate();

            //Διαγραφή των αριθμών της κλήρωσης
            Query query2 = em.createNamedQuery("Winningnumbers.deleteByDrawid");
            query2.setParameter("drawid", parseInt(txbDrawID.getText()));
            query2.executeUpdate();

            //Διαγραφή τω νκερδών της κλήρωσης
            Query query3 = em.createNamedQuery("Prizecategories.deleteByDrawid");
            query3.setParameter("drawid", parseInt(txbDrawID.getText()));
            query3.executeUpdate();

            em.getTransaction().commit();
            //Αν η μέθοδος έχει κληθεί με παράμετρο true δείξε το μήνυμα 
            //Αυτό μπήκε γιατί στην περίπτωση που θα διαγράφει πολλές κληρώσεις θα εμφάνιζε πολλές φορές το μήνυμα και 
            //θα χρειάζοτανε η παρέμβαση του χρήστη για να συνεχιστεί η διαδικασία
            if (RootMessageShow == true) {
                JOptionPane.showMessageDialog(this, "Τα στοιχεία της κλήρωσης διαγράφηκαν!");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Αποτυχία διαγραφής δεδομένων κλήρωσης!");
            em.getTransaction().rollback();
        }
        em.close();
        emf.close();
        
    }
    
    
